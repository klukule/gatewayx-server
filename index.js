// Configure express
app.use(bodyParser.urlencoded({ extended: false }));
app.use(bodyParser.json());
app.use(morgan('dev'));
app.use(passport.initialize());
app.use(allowCrossDomain);

//Connect to mongoDB
mongoose.connect(config.database);

require('./config/passport')(passport);

/*
*
* SOCKET begin
*
*/
io.use(wildcard);

io.on("connection", function (socket) {
  socket.on('room', function(room) {
    socket.join(room);
  });
  socket.on('*', function(data){
    if(data.data[0] != "room"){ //Other event(s) then "room"
      msg = JSON.parse(data.data[1]);
      io.sockets.in(msg.room).emit(data.data[0], data.data.slice(1)); //Send back the data without "function" name
    }
  });
});

/*
*
* API begin
*
*/

var API = require("./API/API.js");
API.Register(require("./API/v1/api.js"));

app.use( '/api', API.Router );

//Server start
http.listen(port, function(){
  console.log( 'GatewayX Server runnig on ' + port + '!' );
});
